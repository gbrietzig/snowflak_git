
  create or replace   view ITAX.GOLD.stg_vendas_clientes
  
  
  
  
  as (
    

SELECT
    CONTRIB_0000.INFO_ROW:REG::string as REG1,
    CONTRIB_0000.INFO_ROW:CNPJ::string as CNPJ1,
    CONTRIB_C010.INFO_ROW:REG::string as REG2,
    CONTRIB_C010.INFO_ROW:CNPJ::string as CNPJ2
FROM
    SILVER.CONTRIB_C010
LEFT JOIN
    SILVER.CONTRIB_C001 ON
        CONTRIB_C001.FILE_ID = CONTRIB_C010.FILE_ID AND
        CONTRIB_C001.ROW_NUMBER = CONTRIB_C010.FATHER_ROW
LEFT JOIN
    SILVER.CONTRIB_0000 ON
        CONTRIB_0000.FILE_ID = CONTRIB_C001.FILE_ID AND
        CONTRIB_0000.ROW_NUMBER = CONTRIB_C001.FATHER_ROW
  );

